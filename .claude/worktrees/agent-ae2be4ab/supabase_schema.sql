create policy "Users can update their own saves." on saves for insert with check (auth.uid() = user_id);
create policy "Users can update their own saves." on saves for update using (auth.uid() = user_id);

-- Leaderboards: Everyone can read, users can only insert/update their own scores
create policy "Anyone can view leaderboard." on leaderboards for select using (true);
create policy "Users can submit their own scores." on leaderboards for insert with check (auth.uid() = user_id);
create policy "Users can update their own scores." on leaderboards for update using (auth.uid() = user_id);
